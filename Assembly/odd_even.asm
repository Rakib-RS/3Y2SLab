.MODEL
.STACK 100H
.DATA
MSG DB "ENTER A NUMBER(Single Digit): $"
msgo DB "ODD$"
msge DB "EVEN$" 
.CODE
MAIN PROC 
MOV AX, @DATA
MOV DS, AX
MOV AH, 9
LEA DX,MSG
INT 21H
;SINGLE CHAR INPUT THROUGH AL


MOV AH,1
INT 21H
MOV BL,AL

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL, 0DH
INT 21H 


TEST BL,1B
JZ EVEN
ODD:
MOV AH, 9
LEA DX,msgo
INT 21H
JMP NEXT
EVEN:
MOV AH, 9
LEA DX,msge
INT 21H
NEXT:

MOV AH,4CH
MAIN ENDP
END MAIN

