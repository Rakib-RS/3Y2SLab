.MODEL
.STACK
.DATA
MSG DB "ENTER A STRING: $"
MSG2 DB "Output: $"
X DB 30 DUP ('\n')

.CODE

MAIN PROC

MOV AX, @DATA
MOV DS,AX
MOV AH,9
LEA DX, MSG
INT 21H
;INPUT CHAR STRING
MOV SI, 0
MOV AH, 1
TOP:
INT 21H
CMP AL, 0DH
JZ EXIT

MOV X[SI],AL
INC SI
JMP TOP
EXIT:

;PRINT NEW LINE
MOV AH,2
MOV DL,0AH
INT 21H
MOV DL, 0DH
INT 21H 

MOV DI, 0
LEVEL:
L1: CMP X[DI],'A'
    JL L
    CMP X[DI],'Z'
    JG L2
    ADD X[DI], 32
    JMP L
L2: CMP X[DI],'a'
    JL L
    CMP X[DI],'z'
    JG L
    SUB X[DI], 32
L:  INC DI
	CMP DI,SI
	JB LEVEL

;Output
	MOV AH, 9
	LEA DX, MSG2
	INT 21H
	
	MOV DI, 0
	MOV AH,2
NEXT2:
	MOV DL, X[DI]
	INT 21H
	INC DI
	CMP DI,SI
	JB NEXT2
	
	MOV AH,4CH
	MAIN ENDP
	END MAIN


